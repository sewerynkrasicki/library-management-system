    private DefaultTableModel dtm(List<Wypozyczenia> wyp){
        SessionFactory sessionFactory = new Configuration().configure().buildSessionFactory(); 
        String[] propNames = sessionFactory.getClassMetadata(Wypozyczenia.class).getPropertyNames();
        ArrayList<String> arrL = new ArrayList<String>();
        arrL.add(sessionFactory.getClassMetadata(Wypozyczenia.class).getIdentifierPropertyName());
        for(int i=0; i<propNames.length; i++){
            arrL.add(propNames[i]);
        }
        String[] columnNames = arrL.toArray(new String[0]);
        DefaultTableModel tab = new DefaultTableModel(columnNames, 0);
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        for(Wypozyczenia w:wyp){
            String oddane;
            if(w.getOddane()==true){
                oddane = "Tak";
            }else{
                oddane = "Nie";
            }
            String returnDate;
            if(w.getData_oddania()==null){
                returnDate = null;
            }else{
                returnDate = formatter.format(w.getData_oddania());
            }
            String[] row = {String.valueOf(w.getId()),String.valueOf(formatter.format(w.getData_wypozyczenia())), returnDate, String.valueOf(w.getKara()),String.valueOf(w.getEgzemplarz().getId()),String.valueOf(w.getCzytelnik().getId()),oddane};
            tab.addRow(row);
        }
        return tab;
    }
    
